const multerStorage = multer.memoryStorage(); // image will be saved as buffer

const multerFilter = (req, file, cb) => {
  if (file.mimetype.startsWith('image')) {
    cb(null, true);
  } else {
    cb(new AppError('PLease Upload an Images Only', 400), false);
  }
};

